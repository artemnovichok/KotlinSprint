package org.example.firstLesson

fun main()
{
    val yearOfFlight: Short = 1961;
    var hourData: Byte;
    var minuteData: Byte;

    //take-off
    hourData = 9;
    minuteData = 7;

    println(yearOfFlight);
    println(hourData);
    println(minuteData);

    //boarding
    hourData = 10;
    minuteData = 55;

    print(hourData);
    print(':');
    print(minuteData);
}